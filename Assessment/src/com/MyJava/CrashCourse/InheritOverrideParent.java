package com.MyJava.CrashCourse;

public class InheritOverrideParent {
	
	protected void MyName() {
		System.out.println("Your Name => ");
		System.out.println(" Me:- My Name is John Wick ");
		
	}
	
	protected void MySalary() {
		System.out.println("What is your annual income => ");
		System.out.println("Me:- Earning to pay income tax ");
		
	}

}
